# Print list , declared in variable
courses = {'English', 'Math', 'Biology', 'Science'}
print(courses)
# Length function
courses = {'English', 'Math', 'Biology', 'Science'}
print(len(courses))
# To know the specific index value list
courses = ["English", "Math", "Biology", "Science"]
print(courses[3])
# To make start and stop index value list
courses = ["English", "Math", "Biology", "Science"]
print(courses[0:2])
# Call particular start index list
courses = ["English", "Math", "Biology", "Science"]
print(courses[2:])
