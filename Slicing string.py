
# Reverse url
sample_url = 'http://coreyms.com'
print(sample_url[::-1])

# To get top level domain
sample_url = 'http://coreyms.com'
print(sample_url[-4:])

# Print url without http://
sample_url = 'http://coreyms.com'
print(sample_url[7:])

# Without http:// or top level of domain
sample_url = 'http://coreyms.com'
print(sample_url[7:-4])
